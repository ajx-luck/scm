#INCLUDE	SC8P1712E.H


GPR0		EQU			?				;用于中断服务中保存ACC
GPR1		EQU			?				;用于中断服务中保存STATUS

ORG			0000H						
JP			START						
ORG			0004H
JP			PB_INT_SERVICE

;-------------------------------------------
START:
		NOP
		CLRWDT
		LDIA		00H
		OPTION							;预分频器分配给TMR0
		CLR			TMR0				;初始化TMR0
		CLRWDT
		
		LDIA		070H				;8M内部时钟
		LD			OSCCON,A
		
;-------------------	
		SETB		WPUB,2				;使能上拉，使RB2为高
		SETB		TRISB,2				;设置RB2的IO口为输入
		SETB		IOCB,2				;允许RB2的IO口电平变化中断
		LDIA		088H				;允许所有未被屏蔽的中断、禁止外设中断，使能PORTB电平变化中断
		LD			INTCON,A
		LD			A,PORTB				;读取PORTB并锁存
;-------------------		
MAIN:									;主程序
		CLRWDT
		
		JP			MAIN
;-------------------------------------------


;-------------------------------------------
;函数名称：PB_INT_SERVICE
;函数功能：中断服务
;入口参数：无
;出口参数：无
;		   
;备	   注：
;-------------------------------------------
PB_INT_SERVICE：
INT_IN:
		LD  		GPR0,A				;中断现场保护
		SWAPR		GPR0
		SWAPA		STATUS
		LD  		GPR1,A					
INT_PB:
		CLRB		INTCON,RBIF			;清中断标志
		LD			A,PORTB
INT_BACK:
		SWAPA		GPR1
		LD  		STATUS,A
		SWAPA		GPR0   				;中断返回
		RETI 				